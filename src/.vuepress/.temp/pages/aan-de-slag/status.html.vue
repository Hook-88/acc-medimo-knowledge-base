<template><div><p>Dit artikel beschrijft de status</p>
<h2 id="status-openen" tabindex="-1"><a class="header-anchor" href="#status-openen"><span>Status openen</span></a></h2>
<p>Er zijn verschillende methoden om de status de openen:</p>
<ul>
<li>Via <strong>Status</strong> in het hoofdmenu.</li>
<li>Met het veld <strong>Zoeken in alles.</strong></li>
<li>Rechtstreeks vanuit een EPD/ECD met een SSO-koppeling.</li>
</ul>
<h3 id="status-in-hoofdmenu" tabindex="-1"><a class="header-anchor" href="#status-in-hoofdmenu"><span>Status in hoofdmenu</span></a></h3>
<p>Via <strong>Status</strong> in het hoofdmenu krijgt u een lijst met cliënten waartoe u toegang hebt. Uw ingestelde zoekvoorkeuren bepalen hoe de lijst met cliënten aan u wordt getoond. U hebt hierbij de keuze uit de volgende opties:</p>
<ul>
<li><strong>Alle patienten:</strong> U krijgt direct een lijst met alle cliënten (van het cluster) waar u toegang tot hebt.</li>
<li><strong>Per afdeling:</strong> Er wordt een lijst met afdelingen getoond waar u toegang tot hebt. Nadat u een afdeling hebt gekozen, wordt een lijst met cliënten getoond.</li>
<li><strong>Per instelling:</strong> Er wordt een lijst met locaties getoond waar u toegang tot hebt. Nadat u een locatie hebt gekozen, wordt een lijst met cliënten getoond.</li>
</ul>
<h3 id="zoeken-in-alles" tabindex="-1"><a class="header-anchor" href="#zoeken-in-alles"><span>Zoeken in alles</span></a></h3>
<p>Bovenin aan de rechterzijde wordt het veld <strong>Zoeken in alles</strong> weergegeven. Hier kunt u zoeken naar de cliënt. Dit kan op naam en op geboortedatum.</p>
<p>Ook wordt in dit veld de laatst geopende statussen getoond, tot maximaal vijf terug.</p>
<h3 id="via-sso-koppeling-uit-ecd-epd" tabindex="-1"><a class="header-anchor" href="#via-sso-koppeling-uit-ecd-epd"><span>Via SSO-koppeling uit ECD/EPD</span></a></h3>
<p>Medimo kan worden gekoppeld aan verschillende ECD's en EPD's. Is er een dergelijke koppeling dan kunt u in het ECD/EPD de cliënt opzoeken en vanuit daar de status openen in Medimo. Raadpleeg documentatie van het ECD/EPD als u wilt weten hoe.</p>
<h2 id="overview-van-de-status" tabindex="-1"><a class="header-anchor" href="#overview-van-de-status"><span>Overview van de Status</span></a></h2>
<p>De status is het medicatiedossier van de cliënt. U kunt daar onder ander de actuele medicatie, indicaties en allergieën nagaan. Daarnaast kan in de status gebruik worden gemaakt van de volgende functionaliteiten:</p>
<ul>
<li>Nieuwe medicatie-opdrachten maken.</li>
<li>Medicatiehistorie</li>
<li>Verschillende medicatielijsten zoals AMO en Medicatieprofiel.</li>
<li>Toedienregistratie en opties.</li>
<li>Medicatie herhalen.</li>
<li>Voorraadbeheer</li>
<li>Medicatie Beoordeling</li>
<li>Acties</li>
<li>Berichten</li>
<li>Meetwaarden</li>
</ul>
<p>Hieronder volgt een toelichting op wat er wordt getoond in de status.</p>
<h2 id="clientgegevens" tabindex="-1"><a class="header-anchor" href="#clientgegevens"><span>Cliëntgegevens</span></a></h2>
<p>Bovenaan worden de cliëntgegevens weergegeven. Ook kunt u vanuit hier het volgende:</p>
<ul>
<li>BEM-score toewijzen.</li>
<li>Pasfoto toevoegen.</li>
<li>Trombosedienst info-scherm openen (mits van toepassing).</li>
<li>Import info-scherm openen (mits van toepassing).</li>
<li>LSP (mits van toepassing).</li>
</ul>
<h2 id="medicatie-meetwaarden-acties-en-berichten" tabindex="-1"><a class="header-anchor" href="#medicatie-meetwaarden-acties-en-berichten"><span>Medicatie, Meetwaarden, Acties en berichten</span></a></h2>
<p>Rechts van de cliëntgegevens wordt, afhankelijk van uw permissies, het volgende getoond:</p>
<ul>
<li>Medicatie: Hiermee kopieert u de actuele medicatie, contra-indicaties en allergieën/intoleranties.</li>
<li>Meetwaarden: Hiermee opent u de meetwaarden van de cliënt.</li>
<li>Acties: Hiermee opent u de openstaande acties bij de cliënt.</li>
<li>Berichten: Hiermee opent u de berichten vastgelegd bij de cliënt.</li>
</ul>
<h2 id="actuele-medicatie" tabindex="-1"><a class="header-anchor" href="#actuele-medicatie"><span>Actuele medicatie</span></a></h2>
<p>Onder de cliëntgegevens wordt de actuele medicatie getoond. Deze wordt in een tabel weergegeven. Hier ziet u wat het gebruik (continu, tijdelijk, zo nodig en schema) is en de dosering. Standaard is deze tabel gesorteerd op geneesmiddelnaam. U kunt deze sortering aanpassen. Klik daarvoor op de tekst <strong>Medicatie</strong>. Achter deze tekst wordt de huidige sortering weergegeven. U kunt de tabel sorteren op het volgende:</p>
<ul>
<li><strong>ABC:</strong> Alphabetisch op naam.</li>
<li><strong>T-ABC:</strong> Gebruik (continu, tijdelijk, zo nodig) gegroepeerd en medicatie alphabetisch op naam gesorteerd.</li>
<li><strong>ATC:</strong> Op ATC-code.</li>
<li><strong>T-ATC:</strong> Gebruik (continu, tijdelijk, zo nodig) gegroepeerd en medicatie alphabetisch op ATC-code gesorteerd.</li>
</ul>
<p>Ook heeft de tabel verschillende weergaves. U kunt met het pil-icoon wissel tussen de weergaves. Hieronder een toelichting op de verschillende weergaves.</p>
<h3 id="actuele-medicatie-weergave-ingang-per-stop-per" tabindex="-1"><a class="header-anchor" href="#actuele-medicatie-weergave-ingang-per-stop-per"><span>Actuele medicatie - Weergave ingang per / stop per</span></a></h3>
<p>In de weergave <strong>Ingang per / Stop per</strong> wordt per medicatieregel de start en, mits bekend, de stop getoond.  Ook kunt u sorteren op deze waardes.</p>
<h3 id="actuele-medicatie-weergave-verstrekkingen" tabindex="-1"><a class="header-anchor" href="#actuele-medicatie-weergave-verstrekkingen"><span>Actuele medicatie - Weergave verstrekkingen</span></a></h3>
<p>De weergave <strong>verstrekkingen</strong> toont de medicatie-aanvragen bij de apotheek. Hier wordt de datum van de laatste aanvraag getoond en, mits berekenbaar, tot welke datum de cliënt voldoende medicatie heeft. Veelal wordt deze weergave gebruikt in de poliklinische setting.</p>
<h3 id="actuele-medicatie-weergave-bijzonderheden" tabindex="-1"><a class="header-anchor" href="#actuele-medicatie-weergave-bijzonderheden"><span>Actuele medicatie - Weergave bijzonderheden</span></a></h3>
<p>Medicatie heeft in Medimo verschillende kenmerken, zoals <strong>in werkvoorraad</strong> of <strong>gekoeld bewaren</strong>. In de weergave bijzonderheden kunt u deze kenmerken nagaan. Deze kenmerken worden als iconen weergegeven aan de rechterzijde. Ga met de muis naar het icoon voor toelichting op de betekenis daarvan.</p>
<h3 id="actuele-medicatie-weergave-schema" tabindex="-1"><a class="header-anchor" href="#actuele-medicatie-weergave-schema"><span>Actuele medicatie - Weergave schema</span></a></h3>
<p>Als (toekomstige) wijziging zijn in de actuele medicatie, dan wordt dit in Medimo als een schema beschouwd. In de weergave schema worden alle (geplande) wijzigingen gegroepeerd en in 1 regel getoond. Dit is handig voor een overzicht van medicatie de dosering zoals de cliënt die op dit moment gebruikt. Alle toekomstige wijzigingen worden niet getoond.</p>
<p>In de gegroepeerde regel wordt de knop <strong>Schema</strong> weergegeven. Hiermee vouwt u het schema open en worden alle toekomstige wijzigingen getoond.</p>
<p>Ook wordt in deze weergave alle gebruiken (continu, tijdelijk, zondig) van dezelfde medicatie gegroepeerd en getoond in één regel.</p>
<h2 id="contra-indicaties-allergieen-intoleranties-en-episodes" tabindex="-1"><a class="header-anchor" href="#contra-indicaties-allergieen-intoleranties-en-episodes"><span>Contra-indicaties, Allergieën/Intoleranties en Episodes</span></a></h2>
<p>Onder de actuele medicatie worden de <strong>contra-indicaties</strong>, <strong>allergieën/intoleranties</strong> en <strong>episodes</strong> weergegeven. Heeft u voorschrijfpermissies, dan kunt u hier de contra-indicaties en allergieën/intoleranties aanpassen.</p>
<p>Voor inzage in- en wijzigen van episodes moet u in het bezit zijn van specifieke permissies. Mist u deze permissies, dan bent u niet gepermitteerd voor de episodes en worden die niet getoond. Dit betreft de onderstaande permissies:</p>
<ul>
<li><strong>Toon episodes</strong></li>
<li><strong>Wijzig episodes</strong></li>
</ul>
<h2 id="knoppen-in-de-status" tabindex="-1"><a class="header-anchor" href="#knoppen-in-de-status"><span>Knoppen in de status</span></a></h2>
<p>Onderaan worden knoppen weergegeven. Daaraan zijn verschillende functies gekoppeld, zoals het voorschrijven van nieuwe medicatie en het openen van de toedienregistratie.</p>
<p>Voor toegang tot deze functies moet u in het bezit zijn van specifieke permissies. Afhankelijk van uw permissies worden hier de volgende knoppen getoond/geactiveerd.</p>
<h3 id="knop-nieuw" tabindex="-1"><a class="header-anchor" href="#knop-nieuw"><span>Knop Nieuw</span></a></h3>
<p>Hiermee voert een voorschrijver nieuwe medicatie in. Ook heeft deze knop een submenu. Hiermee krijgt de voorschrijver toegang tot de volgende voorschrijfopties:</p>
<ul>
<li><strong>Medicatievoorstel</strong></li>
<li><strong>Multistop</strong></li>
<li><strong>Herstart</strong></li>
<li><strong>Top 10 snelstart</strong></li>
<li><strong>Gecombineerd handmatig schema</strong></li>
<li><strong>Ontslagrecept</strong></li>
<li><strong>Faxrecept</strong></li>
<li><strong>Polirecept</strong></li>
</ul>
<h3 id="knop-historie" tabindex="-1"><a class="header-anchor" href="#knop-historie"><span>Knop Historie</span></a></h3>
<p>Alle wijzigingen in-, leveringen van- en meldingen voor de medicatie van de cliënt, zijn na te gaan in de historie. Dit is een overzicht dat bestaat uit veschillende tabbladen.</p>
<h3 id="knop-amo-bmg" tabindex="-1"><a class="header-anchor" href="#knop-amo-bmg"><span>Knop AMO/BMG</span></a></h3>
<p>Hiermee maakt u een AMO/BMG van de cliënt. Dit is een pdf die kan worden geprint in meergegeven aan de cliënt. Deze knop heeft ook een submenu. Hiermee krijgt u toegang tot het volgende:</p>
<ul>
<li><strong>Mail status</strong></li>
<li><strong>Medicatiebewaking</strong></li>
<li><strong>Autorisatielijst</strong></li>
<li><strong>Reisdocument</strong></li>
<li><strong>Medicatieprofiel</strong></li>
<li><strong>Persoonlijk medicatieoverzicht</strong></li>
</ul>
<h3 id="knop-mbo" tabindex="-1"><a class="header-anchor" href="#knop-mbo"><span>Knop MBO</span></a></h3>
<p>In Medimo kunnen <strong>Medicatie Beoordelingen</strong> worden vastgelegd. Deze worden door artsen en apothekers uitgevoerd en worden gestart vanuit de status. Deze om die reden alleen getoond aan artsen en apothekers.</p>
<h3 id="knop-herhaal" tabindex="-1"><a class="header-anchor" href="#knop-herhaal"><span>Knop Herhaal</span></a></h3>
<p>Medicatie dat is ingevoerd kan worden besteld. Dit wordt in Medimo een herhaalaanvraag genoemd. Gebruikers die in het bezit zijn van de permissie: <strong>bestel herhaalmedicatie</strong> kunnen met deze knop een herhaalaanvraag doen. De knop herhaal heeft ook een submenu vanuit daar heeft de gebruiker toegang tot:</p>
<ul>
<li>Herhaal afdeling</li>
<li>Werkvoorraad op naam (onttrekking uit werkvoorraad)</li>
<li>Voorraadbegeer</li>
</ul>
